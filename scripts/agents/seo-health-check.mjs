#!/usr/bin/env node
/* Weekly SEO health snapshot: pulls the live site, checks each route returns 200,
   confirms <title> is unique, JSON-LD parses, OG image loads. Writes a Markdown
   report to reports/seo/<date>.md. */

import { writeFileSync, mkdirSync, existsSync } from 'node:fs'
import { resolve, join } from 'node:path'

const SITE = 'https://ignite-scale.com'
const ROUTES = [
  '/',
  '/services',
  '/services/paid-social',
  '/services/creative',
  '/services/funnels',
  '/dubai-marketing-agency',
  '/meta-ads-dubai',
  '/tiktok-ads-dubai',
  '/web-design-dubai',
  '/results',
  '/process',
  '/book',
  '/faq',
  '/blog/dubai-marketing-best-practices-2026',
  '/blog/meta-ads-mistakes',
  '/blog/tiktok-vs-instagram-ads',
  '/blog/reduce-cost-per-lead',
  '/case-study/dubai-real-estate-4-8x-roas',
  '/case-study/dubai-cosmetics-320-leads',
  '/ar',
  '/ar/services',
  '/ar/meta-ads-dubai',
  '/ar/faq',
  '/og-image.jpg',
  '/sitemap.xml',
  '/robots.txt',
]

const today = new Date().toISOString().slice(0, 10)

async function checkRoute(path) {
  const url = `${SITE}${path}?_t=${Date.now()}`
  const result = { path, url, status: null, title: null, jsonLdBlocks: 0, jsonLdErrors: [], ogImage: null, error: null }
  try {
    const res = await fetch(url, { redirect: 'follow' })
    result.status = res.status
    if (res.ok && (res.headers.get('content-type') || '').includes('text/html')) {
      const html = await res.text()
      const titleMatch = html.match(/<title>([^<]+)<\/title>/)
      result.title = titleMatch ? titleMatch[1] : null
      const ldBlocks = [...html.matchAll(/<script type="application\/ld\+json"[^>]*>([\s\S]*?)<\/script>/g)]
      result.jsonLdBlocks = ldBlocks.length
      for (let i = 0; i < ldBlocks.length; i++) {
        try { JSON.parse(ldBlocks[i][1]) }
        catch (e) { result.jsonLdErrors.push(`Block ${i + 1}: ${e.message}`) }
      }
      const og = html.match(/property="og:image"\s+content="([^"]+)"/)
      result.ogImage = og ? og[1] : null
    }
  } catch (e) {
    result.error = e.message
  }
  return result
}

const results = []
for (const r of ROUTES) {
  process.stdout.write(`  ${r} ... `)
  const r2 = await checkRoute(r)
  results.push(r2)
  console.log(r2.status, r2.error ? `(${r2.error})` : '')
}

/* Aggregations */
const broken = results.filter((r) => !r.error && r.status >= 400)
const errored = results.filter((r) => r.error)
const dupes = (() => {
  const titles = {}
  results.forEach((r) => { if (r.title) (titles[r.title] = titles[r.title] || []).push(r.path) })
  return Object.entries(titles).filter(([_, v]) => v.length > 1)
})()
const schemaErrors = results.flatMap((r) => r.jsonLdErrors.map((e) => `${r.path}: ${e}`))
const missingOg = results.filter((r) => r.title && !r.ogImage)

const reportPath = join(resolve(process.cwd()), 'reports', 'seo', `${today}.md`)
if (!existsSync(resolve('reports/seo'))) mkdirSync(resolve('reports/seo'), { recursive: true })

const sections = []
sections.push(`# SEO health snapshot — ${today}`)
sections.push(``)
sections.push(`Auto-generated by .github/workflows/weekly-seo-monitor.yml`)
sections.push(``)
sections.push(`## Summary`)
sections.push(``)
sections.push(`- Routes checked: **${results.length}**`)
sections.push(`- 4xx / 5xx errors: **${broken.length}**`)
sections.push(`- Fetch errors: **${errored.length}**`)
sections.push(`- Duplicate titles: **${dupes.length}**`)
sections.push(`- Schema JSON-LD errors: **${schemaErrors.length}**`)
sections.push(`- Pages missing og:image: **${missingOg.length}**`)
sections.push(``)

if (broken.length || errored.length || schemaErrors.length || dupes.length || missingOg.length) {
  sections.push(`## ⚠️ Issues to investigate`)
  sections.push(``)
  if (broken.length) {
    sections.push(`### Broken routes`)
    broken.forEach((r) => sections.push(`- \`${r.path}\` → ${r.status}`))
    sections.push(``)
  }
  if (errored.length) {
    sections.push(`### Fetch errors`)
    errored.forEach((r) => sections.push(`- \`${r.path}\` → ${r.error}`))
    sections.push(``)
  }
  if (schemaErrors.length) {
    sections.push(`### Schema errors`)
    schemaErrors.forEach((e) => sections.push(`- ${e}`))
    sections.push(``)
  }
  if (dupes.length) {
    sections.push(`### Duplicate titles`)
    dupes.forEach(([title, paths]) => {
      sections.push(`- "${title}" used on: ${paths.map((p) => `\`${p}\``).join(', ')}`)
    })
    sections.push(``)
  }
  if (missingOg.length) {
    sections.push(`### Pages missing og:image`)
    missingOg.forEach((r) => sections.push(`- \`${r.path}\``))
    sections.push(``)
  }
} else {
  sections.push(`## ✅ All checks passed`)
  sections.push(``)
}

sections.push(`## Full route report`)
sections.push(``)
sections.push(`| Route | Status | Title | JSON-LD blocks |`)
sections.push(`|---|---|---|---|`)
for (const r of results) {
  const title = r.title ? r.title.slice(0, 60) : (r.error ? `error: ${r.error}` : '—')
  sections.push(`| \`${r.path}\` | ${r.status || '—'} | ${title} | ${r.jsonLdBlocks} |`)
}

writeFileSync(reportPath, sections.join('\n'))
console.log(`\n✓ Report written: ${reportPath}`)

if (broken.length || errored.length || schemaErrors.length) {
  console.log(`\n⚠️ Issues detected — review the report.`)
  process.exit(0) // still exit clean so the workflow commits the report
}
