#!/usr/bin/env node
/* Weekly email deliverability health check for ignite-scale.com.
   Verifies SPF, DKIM and DMARC are present and well-formed.
   Checks the domain against common DNS-based blacklists.
   Writes a report to reports/email/<date>.md.
   Run by .github/workflows/weekly-email-deliverability.yml */

import { writeFileSync, mkdirSync, existsSync } from 'node:fs'
import { resolve, join } from 'node:path'
import { promises as dns } from 'node:dns'

const DOMAIN = 'ignite-scale.com'
const DKIM_SELECTOR = 'google._domainkey'
const today = new Date().toISOString().slice(0, 10)

/* Common DNS-based blocklists used by mailbox providers. We query each by appending
   the reverse IP of the sending host. For Google Workspace, the sending IP is in
   the SPF include record _spf.google.com, which resolves to many IPs. We'll do a
   lightweight check on the domain reputation via a few common lookups. */
const BLACKLISTS = [
  { name: 'Spamhaus DBL', dns: 'dbl.spamhaus.org' },
  { name: 'SURBL', dns: 'multi.surbl.org' },
  { name: 'URIBL Black', dns: 'black.uribl.com' },
  { name: 'Spamcop SCBL', dns: 'bl.spamcop.net' },
]

async function checkTxt(host) {
  try {
    const records = await dns.resolveTxt(host)
    return { ok: true, records: records.map((parts) => parts.join('')) }
  } catch (err) {
    return { ok: false, error: err.code || err.message }
  }
}

async function checkBlacklist(blacklist) {
  const query = `${DOMAIN}.${blacklist.dns}`
  try {
    await dns.resolve4(query)
    return { name: blacklist.name, listed: true }
  } catch (err) {
    if (err.code === 'ENOTFOUND' || err.code === 'ENODATA') {
      return { name: blacklist.name, listed: false }
    }
    return { name: blacklist.name, listed: false, error: err.code }
  }
}

async function checkMxRecord() {
  try {
    const mx = await dns.resolveMx(DOMAIN)
    const sortedMx = mx.sort((a, b) => a.priority - b.priority)
    const usingGoogle = sortedMx.some((r) => r.exchange.endsWith('.google.com.') || r.exchange.endsWith('.googlemail.com.'))
    return { ok: true, mx: sortedMx, usingGoogle }
  } catch (err) {
    return { ok: false, error: err.code || err.message }
  }
}

const issues = []

console.log(`Checking email deliverability for ${DOMAIN}...`)

/* SPF check */
const spf = await checkTxt(DOMAIN)
const spfRecord = spf.ok ? spf.records.find((r) => r.startsWith('v=spf1')) : null
if (!spfRecord) {
  issues.push('❌ **No SPF record found.** Mail will land in spam. Add a TXT record at the root with value `v=spf1 include:_spf.google.com ~all`')
} else if (!spfRecord.includes('_spf.google.com')) {
  issues.push(`⚠️ **SPF record exists but does not include Google.** Current value: \`${spfRecord}\`. Should include \`_spf.google.com\`.`)
}

/* DKIM check */
const dkim = await checkTxt(`${DKIM_SELECTOR}.${DOMAIN}`)
const dkimRecord = dkim.ok ? dkim.records.find((r) => r.includes('v=DKIM1')) : null
if (!dkimRecord) {
  issues.push(`❌ **No DKIM record found at ${DKIM_SELECTOR}.${DOMAIN}.** Generate one in Google Workspace admin → Gmail → Authenticate email → Generate new record, then add the TXT record to DNS.`)
} else if (!dkimRecord.includes('k=rsa') || !dkimRecord.includes('p=')) {
  issues.push(`⚠️ **DKIM record looks malformed.** Value: \`${dkimRecord.slice(0, 80)}...\``)
}

/* DMARC check */
const dmarc = await checkTxt(`_dmarc.${DOMAIN}`)
const dmarcRecord = dmarc.ok ? dmarc.records.find((r) => r.startsWith('v=DMARC1')) : null
if (!dmarcRecord) {
  issues.push('❌ **No DMARC record found.** Gmail/Yahoo require this since Feb 2024 for any sender doing volume. Add a TXT record at `_dmarc` with value `v=DMARC1; p=quarantine; rua=mailto:admin@ignite-scale.com; sp=quarantine; aspf=r; adkim=r; fo=1; pct=100`')
} else if (dmarcRecord.includes('p=none')) {
  issues.push(`⚠️ **DMARC policy is set to p=none.** This is monitoring-only. After 2 weeks of confirmed clean delivery, upgrade to p=quarantine.`)
}

/* MX check */
const mx = await checkMxRecord()
if (!mx.ok) {
  issues.push(`❌ **No MX records found.** Mail cannot be delivered to ${DOMAIN}. Add MX records pointing to Google Workspace.`)
} else if (!mx.usingGoogle) {
  issues.push(`⚠️ **MX records do not point to Google Workspace.** Current: ${mx.mx.map((r) => `${r.priority} ${r.exchange}`).join(', ')}`)
}

/* Blacklist checks */
const blacklistResults = await Promise.all(BLACKLISTS.map(checkBlacklist))
const listed = blacklistResults.filter((b) => b.listed)
if (listed.length > 0) {
  for (const l of listed) {
    issues.push(`🚨 **Domain is on blacklist: ${l.name}.** Investigate immediately. This will hard-block deliverability.`)
  }
}

/* Compose report */
const sections = []
sections.push(`# Email deliverability report — ${today}`)
sections.push('')
sections.push(`Auto-generated by .github/workflows/weekly-email-deliverability.yml`)
sections.push('')
sections.push('## Summary')
sections.push('')
if (issues.length === 0) {
  sections.push('✅ **All deliverability checks passed.** SPF, DKIM, DMARC and MX records are configured. Domain is not on any monitored blacklist.')
} else {
  sections.push(`🚨 **${issues.length} issue${issues.length > 1 ? 's' : ''} detected.** Fix in order shown below.`)
  sections.push('')
  for (const issue of issues) sections.push(`- ${issue}`)
}
sections.push('')

sections.push('## Current state')
sections.push('')
sections.push('### SPF (sender authorisation)')
sections.push(spfRecord ? `\`\`\`\n${spfRecord}\n\`\`\`` : 'Not configured.')
sections.push('')
sections.push('### DKIM (cryptographic signature)')
sections.push(dkimRecord ? `Selector: \`${DKIM_SELECTOR}\` configured. Value length: ${dkimRecord.length} chars.` : `Not configured at selector \`${DKIM_SELECTOR}\`.`)
sections.push('')
sections.push('### DMARC (policy)')
sections.push(dmarcRecord ? `\`\`\`\n${dmarcRecord}\n\`\`\`` : 'Not configured.')
sections.push('')
sections.push('### MX (mail server)')
if (mx.ok) {
  sections.push(`Using Google Workspace: ${mx.usingGoogle ? '✅ Yes' : '❌ No'}`)
  sections.push('```')
  for (const r of mx.mx) sections.push(`Priority ${r.priority}: ${r.exchange}`)
  sections.push('```')
} else {
  sections.push(`Error: ${mx.error}`)
}
sections.push('')
sections.push('### Blacklist scan')
sections.push('| Blacklist | Status |')
sections.push('|---|---|')
for (const b of blacklistResults) {
  sections.push(`| ${b.name} | ${b.listed ? '🚨 LISTED' : '✅ Clean'} |`)
}
sections.push('')

/* Write report */
const outDir = resolve('reports/email')
if (!existsSync(outDir)) mkdirSync(outDir, { recursive: true })
const reportPath = join(outDir, `${today}.md`)
writeFileSync(reportPath, sections.join('\n'))

console.log(`\n✓ Report: ${reportPath}`)
console.log(`  Issues found: ${issues.length}`)
if (issues.length > 0) {
  console.log('\nIssues:')
  for (const issue of issues) console.log(`  - ${issue.replace(/\*\*/g, '')}`)
}
